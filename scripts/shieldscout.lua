function script.StartMoving()
	if not walking then
		walking = true
		if GG.lodLevelMedium then
			StartThread(WalkLowDetail)
		else
			StartThread(Walk)
		end
	end
end

function script.StopMoving()
	walking = false
	StartThread(StopWalking)
end

