function widget:GetInfo()
	return {
		name = "UnitResurrected callin",
		author = "Sprung",
		date = "2023-03-11",
		license = "Public Domain",
		layer = math.huge, -- run last so that other widgets receive UnitCreated first (layer inversion)
		enabled = true,
		alwaysStart = true,
		handler = true, -- this is needed to generate custom events
	}
end

--[[ Note that this would ideally be inside the widget handler,
     but it's a very clean and simple example of a custom Lua
     event generated by a widget so I can point modders at it. ]]

function widget:UnitCreated(unitID, unitDefID, teamID, builderID)
	if builderID and Spring.GetUnitCurrentCommand(builderID) == CMD.RESURRECT then
		widgetHandler:UnitResurrected(unitID, unitDefID, teamID, builderID)
	end
end
